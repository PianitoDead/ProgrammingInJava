    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtCodigo.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar el Codigo");
            txtCodigo.requestFocus();
            return ;
        } 
        if(txtNombre.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Nombre");
            txtNombre.requestFocus();
            return ;
        
        } 
        if(txtDireccion.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Direccion");
            txtDireccion.requestFocus();
            return ;
        }
        if(txtCiudad.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Ciudad");
            txtCiudad.requestFocus();
            return ;
        }
        if(txtSueldo.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Sueldo");
            txtSueldo.requestFocus();
            return ;
        }
        if(txtCompania.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Compa√±ia");
            txtCompania.requestFocus();
            return ;
        }
        if(txthorasextra.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane,"Debe ingresar Horas Extra");
            txthorasextra.requestFocus();
            return ;
        }
        
        
       EMPLEADO CLS1=new EMPLEADO(); 
        //bdProductos fun=new bdProductos();
        CLS1.setCodigo_empleado(Integer.parseInt(txtCodigo.getText()));
        CLS1.setNombre(txtNombre.getText());
        CLS1.setDireccion(txtDireccion.getText());
        CLS1.setCiudad(txtCiudad.getText());
        CLS1.setCompania_seguros(txtCompania.getText());
        CLS1.setSueldo(Double.parseDouble(txtSueldo.getText()));
        CLS1.setHoras_extra(Integer.parseInt(txthorasextra.getText()));
        
       
       JOptionPane.showMessageDialog(rootPane, "Registrado Satisfactoriamente");
                String Cadena=CLS1.getCodigo_empleado()+"\n"+CLS1.getNombre()+"\n"+CLS1.getDireccion() +"\n"+ CLS1.getSueldo();
        JOptionPane.showMessageDialog(rootPane, "DATOS GUARDADOS: \n"+Cadena);        
                inhabilitar();
      try {
               
        Vector datos = new Vector();         
        datos.add(Integer.toString(CLS1.getCodigo_empleado()));
        datos.add(CLS1.getNombre());
        datos.add(CLS1.getDireccion()); 
        datos.add(Double.toString(CLS1.getSueldo()));
        modelo.addRow(datos);             
        tablaEmpleados.setModel(modelo);
            centrar_columnas();   
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

