public class Persona {

        private String RUT;
        private String Nombre;

    public Persona() {
        this.RUT = "00000";
    }

    public String getRUT() {
        return RUT;
    }

    public void setRUT(String RUT) {
        this.RUT = RUT;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
       
    
        
    
}
