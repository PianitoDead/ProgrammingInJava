    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if(txtDNI.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el DNI.");
            txtDNI.requestFocus();
            return;
        }

        if(txtNombre.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Nombre.");
            txtNombre.requestFocus();
            return;
        }

        if(txtDirrecion.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar la Dirrecion.");
            txtDirrecion.requestFocus();
            return;
        }

        if(txtFecha_Nac.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar la Fecha de Nacimiento.");
            txtFecha_Nac.requestFocus();
            return;
        }


        if(txtDiagnostico.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Diagnostico.");
            txtDiagnostico.requestFocus();
            return;
        }

        if(txtFactura.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar la Factura.");
            txtFactura.requestFocus();
            return;
        }

        Paciente PAC1 = new Paciente ();
        PAC1.setDNI(txtDNI.getText());
        PAC1.setNombre(txtNombre.getText());
        PAC1.setDirrecion(txtDirrecion.getText());
        PAC1.setFecha_Nac(txtFecha_Nac.getText());
        PAC1.setDiagnostico(txtDiagnostico.getText());
        PAC1.setFactura(Double.parseDouble(txtFactura.getText()));

        JOptionPane.showMessageDialog(rootPane, "Registro Exitoso.");

        String Cadena= "\nDNI: " + PAC1.getDNI() + "\nNombre: " + PAC1.getNombre() + "\nDirrecion: " + PAC1.getDirrecion() +
        "\nFecha de Nacimiento: " + PAC1.getFecha_Nac() + "\nDiagnostico: " + "\n" + PAC1.getDiagnostico() +
        "\nFactura: " + PAC1.getFactura();
        JOptionPane.showMessageDialog(rootPane, "Datos Guardados" + Cadena);

        Inhabilitar();

    }//GEN-LAST:event_btnGuardarActionPerformed

