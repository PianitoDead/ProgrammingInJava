    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if(txtDNI.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el DNI.");
            txtDNI.requestFocus();
            return;
        }

        if(txtNombre.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Nombre.");
            txtNombre.requestFocus();
            return;
        }

        if(txtDirrecion.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar la Dirrecion.");
            txtDirrecion.requestFocus();
            return;
        }

        if(txtFecha_Nac.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar la Fecha de Nacimiento.");
            txtFecha_Nac.requestFocus();
            return;
        }

        if(txtCodigo.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Codigo.");
            txtCodigo.requestFocus();
            return;
        }

        if(txtFax.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Fax.");
            txtFax.requestFocus();
            return;
        }
        
        if(txtTelefono.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Telefono.");
            txtTelefono.requestFocus();
            return;
        }
        
        if(txtDescuentos.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Descuento.");
            txtDescuentos.requestFocus();
            return;
        }
        
        if(txtSaldo.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar el Saldo.");
            txtSaldo.requestFocus();
            return;
        }

        Proveedor PRO1 = new Proveedor ();
        PRO1.setDNI(txtDNI.getText());
        PRO1.setNombre(txtNombre.getText());
        PRO1.setDirrecion(txtDirrecion.getText());
        PRO1.setFecha_Nac(txtFecha_Nac.getText());
        PRO1.setCodigo(txtCodigo.getText());
        PRO1.setFax(txtFax.getText());
        PRO1.setTelefono(txtTelefono.getText());
        PRO1.setDescuentos(Double.parseDouble(txtDescuentos.getText()));
        PRO1.setSaldo(Double.parseDouble(txtSaldo.getText()));


        JOptionPane.showMessageDialog(rootPane, "Registro Exitoso.");

         String Cadena= "\nDNI: " + PRO1.getDNI() + "\nNombre: " + PRO1.getNombre() + "\nDirrecion: " + PRO1.getDirrecion() +
        "\nFecha de Nacimiento: " + PRO1.getFecha_Nac() + "\nCOdigo: " + PRO1.getCodigo() +
        "\nFax: " + PRO1.getFax() + "\nTelefono: " + PRO1.getTelefono() + "\nDescuentos: " + PRO1.getDescuentos() + "\nSaldo: " + PRO1.getSaldo();
         
        JOptionPane.showMessageDialog(rootPane, "Datos Guardados" + Cadena);

        Inhabilitar();
    }//GEN-LAST:event_btnGuardarActionPerformed

