public class Vehiculo {
    
    String Patente, Marca;

    public Vehiculo() {
        
        this.Patente = "N";
        this.Marca = "M";
    }

    public String getPatente() {
        return Patente;
    }

    public void setPatente(String Patente) {
        this.Patente = Patente;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }
    
    
}
